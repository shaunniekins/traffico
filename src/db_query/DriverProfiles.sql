create table public."DriverProfiles" (
    id bigint generated by default as identity,
    last_name text null,
    first_name text null,
    middle_name text null,
    birth_date date null,
    birth_place text null,
    address text null,
    civil_status text null,
    blood_type text null,
    contact_num text null,
    license_num text null,
    license_expiration date null,
    is_active boolean null,
    contact_relationship_driver text null,
    contact_person text null,
    contact_person_num text null,
    contact_address text null,
    face_photo text null,
    signature_photo text null,
    created_at timestamp with time zone null default (now() at time zone 'utc' :: text),
    constraint driverprofiles_pkey primary key (id)
) tablespace pg_default;